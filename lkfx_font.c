/* ================================================================
 * Font engine — bitmap ROW-MAJOR, MSB first
 *
 * pixel(row, col):
 *   bytes_per_row = (width + 7) / 8
 *   byte  = bitmap[offset + row * bytes_per_row + col / 8]
 *   pixel = (byte >> (7 - col % 8)) & 1
 * ================================================================ */
int lkfx_char(int x, int y, char c,
               const lkfx_font_t *font, int scale,
               uint16_t fg, uint16_t bg) {
    if (!font || scale < 1) return 0;

    uint8_t uc = (uint8_t)c;
    if (uc < font->first_char || uc > font->last_char) {
        int adv = font->glyph[0].x_advance * scale;
        if (bg != LKFX_TRANSPARENT)
            for (int row = 0; row < font->height * scale; row++)
                for (int col = 0; col < adv; col++)
                    lkfx_fb_set_safe(x+col, y+row, bg);
        return adv;
    }

    const lkfx_glyph_t *g   = &font->glyph[uc - font->first_char];
    const uint8_t      *bmp = font->bitmap + g->bitmap_offset;
    int gw            = g->width;
    int gh            = g->height;
    int bytes_per_row = (gw + 7) / 8;

    for (int row = 0; row < gh; row++) {
        for (int col = 0; col < gw; col++) {
            int bit = (bmp[row * bytes_per_row + col / 8] >> (7 - col % 8)) & 1;
            uint16_t color = bit ? fg : bg;
            if (color == LKFX_TRANSPARENT) continue;
            for (int sy = 0; sy < scale; sy++)
                for (int sx = 0; sx < scale; sx++)
                    lkfx_fb_set_safe(x + col*scale + sx,
                                     y + row*scale + sy,
                                     color);
        }
    }

    /* Fill padding bên phải */
    int drawn_w = gw * scale;
    int advance = g->x_advance * scale;
    if (bg != LKFX_TRANSPARENT && drawn_w < advance) {
        for (int row = 0; row < gh * scale; row++)
            for (int col = drawn_w; col < advance; col++)
                lkfx_fb_set_safe(x+col, y+row, bg);
    }

    return advance;
}

int lkfx_text(int x, int y, const char *str,
               const lkfx_font_t *font, int scale,
               uint16_t fg, uint16_t bg) {
    if (!str || !font) return x;
    while (*str) {
        x += lkfx_char(x, y, *str, font, scale, fg, bg);
        str++;
        if (x >= LKFX_W) break;
    }
    return x;
}

void lkfx_text_center(int cx, int y, const char *str,
                       const lkfx_font_t *font, int scale,
                       uint16_t fg, uint16_t bg) {
    int w = lkfx_text_width(str, font, scale);
    lkfx_text(cx - w/2, y, str, font, scale, fg, bg);
}

void lkfx_text_right(int rx, int y, const char *str,
                      const lkfx_font_t *font, int scale,
                      uint16_t fg, uint16_t bg) {
    int w = lkfx_text_width(str, font, scale);
    lkfx_text(rx - w, y, str, font, scale, fg, bg);
}

int lkfx_text_width(const char *str, const lkfx_font_t *font, int scale) {
    if (!str || !font) return 0;
    int w = 0;
    while (*str) {
        uint8_t uc = (uint8_t)*str;
        int idx = (uc >= font->first_char && uc <= font->last_char)
                  ? uc - font->first_char : 0;
        w += font->glyph[idx].x_advance * scale;
        str++;
    }
    return w;
}

int lkfx_text_height(const lkfx_font_t *font, int scale) {
    if (!font) return 0;
    return font->height * scale;
}

int lkfx_text_wrap(int x, int y, int w, int h,
                    const char *str,
                    const lkfx_font_t *font, int scale,
                    uint16_t fg, uint16_t bg) {
    if (!str || !font) return 0;
    int line_h = font->height * scale;
    int cx = x, cy = y, lines = 0;

    while (*str && cy + line_h <= y + h) {
        if (*str == '\n') {
            cx = x; cy += line_h; lines++; str++; continue;
        }
        /* Đo từ tiếp theo */
        const char *p = str;
        int word_w = 0;
        while (*p && *p != ' ' && *p != '\n') {
            uint8_t uc = (uint8_t)*p;
            int idx = (uc >= font->first_char && uc <= font->last_char)
                      ? uc - font->first_char : 0;
            word_w += font->glyph[idx].x_advance * scale;
            p++;
        }
        if (cx > x && cx + word_w > x + w) {
            cx = x; cy += line_h; lines++;
            if (cy + line_h > y + h) break;
        }
        cx += lkfx_char(cx, cy, *str, font, scale, fg, bg);
        str++;
    }
    return lines + 1;
}
